int	ft_str_is_numeric(char *str)
{
	int	valid;

	valid = 1;
	while (*str != '\0')
	{
		if (!(*str >= '0' && *str <= '9'))
		{
			valid = 0;
			break ;
		}
		str++;
	}
	return (valid);
}
