int	ft_str_is_lowercase(char *str)
{
	int	valid;

	valid = 1;
	while (*str != '\0')
	{
		if (!(*str >= 'a' && *str <= 'z'))
		{
			valid = 0;
			break ;
		}
		str++;
	}
	return (valid);
}
